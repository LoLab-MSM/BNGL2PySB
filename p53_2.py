from pysb import *
Model()
Parameter('ksynMdm2', 0.000495)
Parameter('kdegMdm2', 0.000433)
Parameter('ksynp53', 0.006)
Parameter('kdegp53', 0.000825)
Parameter('kbinMdm2p53', 0.001155)
Parameter('krelMdm2p53', 1.155e-05)
Parameter('ksynMdm2mRNA', 0.0001)
Parameter('kdegMdm2mRNA', 0.0001)
Parameter('kactATM', 0.0001)
Parameter('kdegATMMdm2', 0.0004)
Parameter('kinactATM', 0.0005)
Parameter('kphosp53', 0.0005)
Parameter('kdephosp53', 0.5)
Parameter('kphosMdm2', 2.0)
Parameter('kdephosMdm2', 0.5)
Parameter('kdam', 0.08)
Parameter('krepair', 2e-05)
Parameter('kproteff', 1.0)
Parameter('ksynp53mRNA', 0.001)
Parameter('kdegp53mRNA', 0.0001)
Parameter('IR', 0)
Monomer('Mdm2')
Monomer('p53')
Monomer('Mdm2_p53')
Monomer('Mdm2_mRNA')
Monomer('p53_mRNA')
Monomer('ATMA')
Monomer('ATMI')
Monomer('p53_P')
Monomer('Mdm2_P')
Monomer('damDNA')
Observable("obs_Mdm2", Mdm2())
Observable("obs_p53", p53())
Observable("obs_Mdm2_p53", Mdm2_p53())
Observable("obs_Mdm2_mRNA", Mdm2_mRNA())
Observable("obs_p53_mRNA", p53_mRNA())
Observable("obs_ATMA", ATMA())
Observable("obs_ATMI", ATMI())
Observable("obs_p53_P", p53_P())
Observable("obs_Mdm2_P", Mdm2_P())
Observable("obs_damDNA", damDNA())
Initial(Mdm2(), Parameter("Mdm2_0",5.0))
Initial(p53(), Parameter("p53_0",5.0))
Initial(Mdm2_p53(), Parameter("Mdm2_p53_0",95.0))
Initial(Mdm2_mRNA(), Parameter("Mdm2_mRNA_0",10.0))
Initial(p53_mRNA(), Parameter("p53_mRNA_0",10.0))
Initial(ATMI(), Parameter("ATMI_0",200.0))
Expression("functionRate6", kdegMdm2 * kproteff )
Expression("functionRate8", kdegp53 * kproteff )
Expression("functionRate11", kdam * IR )
Rule("p53mRNASynthesis", None  >>  p53_mRNA() , ksynp53mRNA)
Rule("p53mRNADegradation", p53_mRNA()  >>  None , kdegp53mRNA)
Rule("Mdm2Synthesis", Mdm2_mRNA()  >>  Mdm2_mRNA() + Mdm2()  , ksynMdm2)
Rule("Mdm2mRNASynthesis1", p53()  >>  p53() + Mdm2_mRNA() , ksynMdm2mRNA)
Rule("Mdm2mRNASynthesis2", p53_P()  >>  p53_P() + Mdm2_mRNA()  , ksynMdm2mRNA)
Rule("Mdm2mRNADegradation", Mdm2_mRNA()  >>  None  , kdegMdm2mRNA)
Rule("Mdm2Degradation", Mdm2()  >>  None  , functionRate6)
Rule("p53Synthesis", p53_mRNA()  >>  p53() + p53_mRNA()  , ksynp53)
Rule("p53Degradation", Mdm2_p53()  >>  Mdm2()  , functionRate8)
Rule("P53_Mdm2Binding", p53() + Mdm2()  >>  Mdm2_p53() , kbinMdm2p53)
Rule("P53_Mdm2Release", Mdm2_p53()  >>  p53() + Mdm2() , krelMdm2p53)
Rule("DNAdamage", None   >>  damDNA() , functionRate11)
Rule("DNArepair", damDNA()  >>  None , krepair)
Rule("ATMactivation", damDNA() + ATMI()  >>  damDNA() + ATMA() , kactATM)
Rule("p53phoshorylation", p53() + ATMA()  >>  p53_P() + ATMA() , kphosp53)
Rule("p53dephosorylation", p53_P()  >>  p53() , kdephosp53)
Rule("Mdm2phoshorylation", Mdm2() + ATMA()  >>  Mdm2_P() + ATMA() , kphosMdm2)
Rule("Mdm2dephosorylation", Mdm2_P()  >>  Mdm2() , kdephosMdm2)
Rule("Mdm2Pdegradation", Mdm2_P()  >>  None  , kdegATMMdm2)
Rule("ATMInactivation", ATMA()  >>  ATMI() , kinactATM)
